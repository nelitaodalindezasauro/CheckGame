
package model;

public class ponte {
    private String gpu;
    private String jogo;

    public ponte(String gpu, String jogo) {
        this.gpu = gpu;
        this.jogo = jogo;
    }

    public String getGpu() {
        return gpu;
    }

    public void setGpu(String gpu) {
        this.gpu = gpu;
    }

    public String getJogo() {
        return jogo;
    }

    public void setJogo(String jogo) {
        this.jogo = jogo;
    }
}
